class Testlog {
  static int _index = 0;

  static SmashPosition getNext(KalmanFilter filter) {
    if (_index > TESTLOG.length - 2) {
      _index = 0;
    }
    var data1 = TESTLOG[_index];
    var data2 = TESTLOG[_index + 1];
    var c1 = Coordinate(data1[0].toDouble(), data1[1].toDouble());
    var c2 = Coordinate(data2[0].toDouble(), data2[1].toDouble());
    var azimuth = _azimuth(c1, c2);
    var altim = data1[2];
    var ts = data1[3].toDouble();
    var accura = data1[4];
    var speedMs = data1[5];
    _index++;

    filter.process(c2.y, c2.x, accura.toDouble(), ts, speedMs.toDouble());

    return SmashPosition.fromJson({
      ARG_LATITUDE: c2.y,
      ARG_LONGITUDE: c2.x,
      ARG_ALTITUDE: altim.toDouble(),
      ARG_LATITUDE_FILTERED: filter.latitude,
      ARG_LONGITUDE_FILTERED: filter.longitude,
      ARG_ACCURACY_FILTERED: filter.accuracy,
      ARG_HEADING: azimuth,
      ARG_ACCURACY: accura,
      ARG_TIME: ts,
      ARG_SPEED: speedMs,
      ARG_SPEED_ACCURACY: 1.0,
      ARG_MOCKED: true,
    });
  }

  /// Calculates the azimuth in degrees given two {@link Coordinate} composing a line.
  ///
  /// Note that the coords order is important and will differ of 180.
  ///
  /// @param c1 first coordinate (used as origin).
  /// @param c2 second coordinate.
  /// @return the azimuth angle.
  static double _azimuth(Coordinate c1, Coordinate c2) {
    // vertical
    if (c1.x == c2.x) {
      if (c1.y == c2.y) {
        // same point
        return double.nan;
      } else if (c1.y < c2.y) {
        return 0.0;
      } else if (c1.y > c2.y) {
        return 180.0;
      }
    }
    // horiz
    if (c1.y == c2.y) {
      if (c1.x < c2.x) {
        return 90.0;
      } else if (c1.x > c2.x) {
        return 270.0;
      }
    }
    // -> /
    if (c1.x < c2.x && c1.y < c2.y) {
      double tanA = (c2.x - c1.x) / (c2.y - c1.y);
      double atanD = atan(tanA);
      return MercatorUtils.radianToDeg(atanD);
    }
    // -> \
    if (c1.x < c2.x && c1.y > c2.y) {
      double tanA = (c1.y - c2.y) / (c2.x - c1.x);
      double atanD = atan(tanA);
      return MercatorUtils.radianToDeg(atanD) + 90.0;
    }
    // <- /
    if (c1.x > c2.x && c1.y > c2.y) {
      double tanA = (c1.x - c2.x) / (c1.y - c2.y);
      double atanD = atan(tanA);
      return MercatorUtils.radianToDeg(atanD) + 180;
    }
    // <- \
    if (c1.x > c2.x && c1.y < c2.y) {
      double tanA = (c2.y - c1.y) / (c1.x - c2.x);
      double atanD = atan(tanA);
      return MercatorUtils.radianToDeg(atanD) + 270;
    }

    return double.nan;
  }
}

class TestLogStream {
  static final TestLogStream _singleton = TestLogStream._internal();
  factory TestLogStream() {
    return _singleton;
  }
  TestLogStream._internal() {
    if (_streamSubscription == null) {
      _streamSubscription = _controller.stream.listen((event) {});
    }
  }

  StreamSubscription<SmashPosition>? _streamSubscription;
  final kalman = KalmanFilter();
  StreamController<SmashPosition> _controller =
      StreamController<SmashPosition>();
  Timer? timer;
  bool isActive = false;

  int millisecondsDuration = 500;

  setNewDuration(int durationMillis) {
    millisecondsDuration = durationMillis;
    stop();
    start();
  }

  void start() {
    if (timer != null) {
      return;
    }
    isActive = true;
    timer = Timer.periodic(Duration(milliseconds: millisecondsDuration), (t) {
      _controller.sink.add(Testlog.getNext(kalman));
    });
  }

  StreamSubscription<SmashPosition>? get streamSubscription {
    return _streamSubscription;
  }

  void stop() {
    if (timer != null) {
      isActive = false;
      timer?.cancel();
      timer = null;
    }
  }

  closeStream() {
    _controller.close();
  }
}

